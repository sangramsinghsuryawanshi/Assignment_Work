import java.util.*;
public class DisplayNameFiveTimes
{
	public static void main(String[]ar)
	{
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter given number:");
		int n = sc.nextInt();
		System.out.println("Entered name Repeated upto five times:");
		int i = 1;
		while(i<=n)
		{	
			System.out.println("Suryawanshi Sangramsingh");
			i++;
		}
	}
}